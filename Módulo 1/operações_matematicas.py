# Tipos de números que podem ser usados em Python

a = 20
b = 20.5
print(type(a))
print(type(b))

print(10 + 6)
print(10 - 6)
print(10 * 6)
print(10 / 6)
print(10 // 6) # divisão de inteiro
print(10 % 6)  # modulos
print(10 ** 6) # exponenciais

# Atalho para atribuição mais rápida

a = 10
a = a + 10
a += 5

b = 20
b = b - 2
b -= 2

